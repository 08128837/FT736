require(data.table)

# 建立一般的data.frame
theDF <- data.frame(A=1:10,
                    B=letters[1:10],
                    C=LETTERS[11:20],
                    D=rep(c("One", "Two", "Three"), length.out=10))

# 建立一個data.table
theDT <- data.table(A=1:10,
                    B=letters[1:10],
                    C=LETTERS[11:20],
                    D=rep(c("One", "Two", "Three"), length.out=10))

# 把它們顯示出來,再做比較
theDF
theDT

# 可以注意到data.frame的預設設定會把character資料轉換成factor
# 而data.table不會這麼做
